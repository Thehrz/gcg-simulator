export const getIcon = (id: string) => {
  return `/images/icons/${id}.png`
}

export const getCard = (id: string) => {
  return `/images/cards/${id}.png`
}

export const getSkill = (id: string, index: number) => {
  return `/images/skills/${id}/skill_${index}.png`
}

export const getElement = (element: string) => {
  return `/images/icons/elements/${element}.png`
}

